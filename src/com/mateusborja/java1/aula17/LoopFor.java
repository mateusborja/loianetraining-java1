package com.mateusborja.java1.aula17;

public class LoopFor {

	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) {
			System.out.println("valor de i = " + i);
		}

		System.out.println();

		for (int j = 5; j >= 0; j--) {
			System.out.println("valor de i = " + j);

		}

	}

}
